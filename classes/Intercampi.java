public class Intercampi {
    public String[] onibus;
    public String[][] horarios;

    public Intercampi() { }

    //---------------------------

    public String[] getOnibus() {
        return this.onibus;
    }

    public void setOnibus(String[] _onibus) {
        this.onibus = _onibus;
    }

    //--

    public String[][] getHorarios() {
        return this.horarios;
    }

    public void setHorarios(String[][] _horarios) {
        this.horarios = _horarios;
    }

    //---------------------------
}
